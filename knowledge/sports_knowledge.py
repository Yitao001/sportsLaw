def get_sport_info(sport_name: str) -> dict | None:
    """
    查询指定运动的百科信息
    Args:
        sport_name: 运动名称（中文）
    Returns:
        运动信息字典，未找到则返回None
    """
    # 精确匹配
    if sport_name in SPORTS_ENCYCLOPEDIA:
        return SPORTS_ENCYCLOPEDIA[sport_name]

    # 模糊匹配
    sport_name_lower = sport_name.lower()
    for key, value in SPORTS_ENCYCLOPEDIA.items():
        if sport_name_lower in key.lower() or sport_name_lower in value.get("英文名", "").lower():
            return {**value, "_matched_key": key}

    return None


def list_all_sports() -> list[str]:
    """返回所有已知运动的名称列表"""
    return list(SPORTS_ENCYCLOPEDIA.keys())


def format_sport_info(sport_name: str) -> str:
    """
    格式化输出指定运动的百科信息
    Args:
        sport_name: 运动名称
    Returns:
        格式化的运动介绍文本
    """
    info = get_sport_info(sport_name)
    if not info:
        available = "、".join(list_all_sports())
        return f"暂未收录「{sport_name}」的详细信息。\n\n目前已收录的运动项目包括：{available}\n\n如需了解其他运动，欢迎告知，我们会为您检索相关信息。"

    matched_name = info.get("_matched_key", sport_name)
    lines = [f"## {matched_name}（{info.get('英文名', '')}）\n"]

    field_labels = {
        "管理机构": "【管理机构】",
        "规则简介": "【规则简介】",
        "主要赛事": "【主要赛事】",
        "法律特点": "【法律特点】",
        "奥运状态": "【奥运状态】",
        "职业化程度": "【职业化程度】",
    }

    for field, label in field_labels.items():
        value = info.get(field)
        if value is None:
            continue
        if isinstance(value, list):
            lines.append(f"**{label}**：")
            for item in value:
                lines.append(f"  - {item}")
        else:
            lines.append(f"**{label}**：{value}")
        lines.append("")

    return "\n".join(lines)
